		for t=1,#nNova do
			if nNova[t].range == 1 then
				for i=1,#BubbleDebuffs do
					if not UnitDebuffID(nNova[t].unit, 6788) and UnitDebuffID(nNova[t].unit,BubbleDebuffs[i].ID) then
