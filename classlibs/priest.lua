	function CML.Will(value)
		local _ActiveWill = _G[PQIprefix.."ActiveInnerWill_enable"]
		local _ActiveWillValue = _G[PQIprefix.."ActiveInnerWill_value"]
		if GetUnitSpeed("player") > 0 then
			if IsRunning == nil then
				IsRunning = GetTime()
				IsStanding = nil
			end
		else
			if IsStanding == nil then
				IsStanding = GetTime()
				IsRunning = nil
			end
		end
		if IsRunning ~= nil and _ActiveWill and value == 2 then
			if _ActiveWillValue < GetTime() - IsRunning then
				IsRunning = nil
				return true
			end
		end
		if IsStanding ~= nil and _ActiveWill and value == 1 then
			if _ActiveWillValue < GetTime() - IsStanding then
				IsStanding = nil
				return true
			end
		end
	end	

end

